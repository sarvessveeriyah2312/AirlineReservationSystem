    private void initData() {
        URL url;
        try {
            url = new URL(reservation.getTicket().getFlight().getImageUrl());
            BufferedImage bufferedImage = ImageIO.read(url);
            ImageIcon icon = new ImageIcon(bufferedImage);
            Image imageFlight = icon.getImage();
            imageFlight = imageFlight.getScaledInstance(299, 166,
                    Image.SCALE_SMOOTH);

            imgFlightImage.setIcon(new ImageIcon(imageFlight));

        } catch (MalformedURLException ex) {
            Logger.getLogger(FlightItem.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FlightItem.class.getName()).log(Level.SEVERE, null, ex);
        }

        txtFlightNo.setText(reservation.getTicket().getFlight().getFlightNo());
        txtTotalSeats.setText(String.valueOf(reservation.getTicket().getFlight().getTotalSeats()));
        txtBoardingDate.setText(reservation.getTicket().getFlight().getFlightNo());
        txtFrom.setText(reservation.getTicket().getFlight().getOrigin().getValue());
        txtTo.setText(reservation.getTicket().getFlight().getDestination().getValue());
        txtPrice.setText(String.valueOf(reservation.getTicket().getFlight().getPrice()));
        txtTotal.setText(String.valueOf(reservation.getTicket().calcTotalPrice()));
    }

