    public void resetTotal() {
        if (!etWeight.getText().equals("")) {
            reservation.getTicket().setLuggageWeight(Double.parseDouble(etWeight.getText()));
            reservation.getTicket().setIsTakeInsurance(etYes.isSelected());
//            Recalculate Price
            txtTotal.setText(String.valueOf(reservation.getTicket().calcTotalPrice()));
        }
    }
